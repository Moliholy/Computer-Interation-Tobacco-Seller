    private void actualizarTablaModificarproducto() {
        DefaultTableModel model = (DefaultTableModel) tablaModificarProducto.getModel();
        model.setRowCount(0);
        ArrayList<Producto> pedidos = new ArrayList<>(controlador.getProductos().values());
        Collections.sort(pedidos);
        //ya los tenemos ordenados todos, ahora los mostramos
        for (int i = 0; i < pedidos.size(); i++) {
            Producto p = pedidos.get(i);
            Object[] datos = new Object[]{p.getCodigo(), p.getNombre(), p.getFormato().toString(), p.getPrecio(), p.getStock()};
            model.addRow(datos);
        }
        model.fireTableDataChanged();
    }

    private void tablaModificarProductoModificada(TableModelEvent e) {
        DefaultTableModel model = (DefaultTableModel) tablaModificarProducto.getModel();
        int fila = e.getFirstRow();
        int columna = e.getColumn();
        if (columna >= 1) {
            Long codigo = (Long) model.getValueAt(fila, 0);
            String nombre = (String) model.getValueAt(fila, 1);
            FormatoProducto formato = FormatoProducto.valueOf(((String) model.getValueAt(fila, 2)).toUpperCase());
            Float precio = (Float) model.getValueAt(fila, 3);
            Integer stock = (Integer) model.getValueAt(fila, 4);
            controlador.setValoresProducto(codigo, nombre, formato, precio, stock);
        }
    }

