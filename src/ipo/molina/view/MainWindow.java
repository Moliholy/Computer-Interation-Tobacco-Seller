    private void actualizarTextoPagado() {
        if (controlador.getPedidoActual() != null
                && !txtPagadoUnidades.getText().equals("")
                && !txtPagadoDecimales.getText().equals("")) {
            Float pagadoUnidades = Float.valueOf(txtPagadoUnidades.getText());
            Float pagadoDecimales = Float.valueOf(txtPagadoDecimales.getText());
            float pagado = pagadoUnidades + 0.01f * pagadoDecimales;
            controlador.setPagado(pagado);
            actualizarNumeros();
        }
    }

    private void ajustarTextoPorDefecto(JTextField txt, String textoPorDefecto) {
        if (txt.getText().equals(textoPorDefecto)) {
            txt.setText("");
        } else if (txt.getText().equals("")) {
            txt.setText(textoPorDefecto);
        }
    }

    private void filtrarCaracteresNoNumericos(java.awt.event.KeyEvent evt) {
        char c = evt.getKeyChar();
        if ((c >= 58 && c <= 126) || (c >= 32 && c <= 47)) {
            evt.consume();
        }
    }

    public void actualizarTodo() {
        actualizarTablaPedidos();
        actualizarNumeros();
    }

    public void actualizarNumeros() {
        DecimalFormat df = new DecimalFormat("0.00");
        //si hay algún pedido abierto actualizamos los números
        if (controlador.getPedidoActual() != null) {
            float precioTotal = controlador.getPedidoActual().calcularPrecio();
            txtCosteTotal.setText(df.format(precioTotal));
            float pagado = controlador.getPagado();
            float devolucion = Math.max(0, pagado - precioTotal);
            txtDevolucion.setText(df.format(devolucion));
            //vamos ahora a por el texto de "Pagado"
            String txtPagado = df.format(pagado);
            String[] partes = txtPagado.split(",");
            txtPagadoUnidades.setText(partes[0]);
            txtPagadoDecimales.setText(partes[1]);
        }
    }

