    private String identidicarRadioButtons() {
        if (rbBolsa.isSelected()) {
            return FormatoProducto.BOLSA.toString();
        } else if (rbBote.isSelected()) {
            return FormatoProducto.BOTE.toString();
        } else if (rbCaja.isSelected()) {
            return FormatoProducto.CAJA.toString();
        } else if (rbCajetilla.isSelected()) {
            return FormatoProducto.CAJETILLA.toString();
        } else if (rbEstuche.isSelected()) {
            return FormatoProducto.ESTUCHE.toString();
        } else if (rbLata.isSelected()) {
            return FormatoProducto.LATA.toString();
        } else if (rbUnidad.isSelected()) {
            return FormatoProducto.UNIDAD.toString();
        }
        return null;
    }

