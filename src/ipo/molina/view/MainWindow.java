    private void botonEliminarEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarEliminarProductoActionPerformed
        // TODO add your handling code here:
        Producto p = controlador.buscarProducto(txtCodigoEliminarProducto.getText());
        if (p != null) {
            int respuesta = JOptionPane.showConfirmDialog(dlgEliminarProducto,
                    "¿Está seguro de que desea eliminar este producto?",
                    "Confirmación para la eliminación",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (respuesta == JOptionPane.OK_OPTION) {
                Pedido actual = controlador.getPedidoActual();
                if (actual == null || !actual.getProductos().contains(p)) {
                    controlador.eliminarProducto(p);
                } else {
                    mostrarMensajeError("Error: el producto que está tratando de eliminar se encuentra en el pedido actual");
                }
            }
        }
        panelDatosEliminacionPedido.setText("");
        txtCodigoEliminarProducto.setText("");
        botonEliminarEliminarProducto.setEnabled(false);
        dlgEliminarProducto.setVisible(false);
    }//GEN-LAST:event_botonEliminarEliminarProductoActionPerformed

