    private void tablaCompraModificada(TableModelEvent e) {
        int fila = e.getFirstRow();
        int columna = e.getColumn();
        DefaultTableModel model = (DefaultTableModel) tablaCompra.getModel();
        Integer nuevaCantidad = (Integer) model.getValueAt(fila, columna);
        controlador.getPedidoActual().getProductos().get(fila).setCantidad(nuevaCantidad);
        actualizarTodo();
    }

