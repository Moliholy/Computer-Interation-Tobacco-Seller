    private void menuSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuSalirActionPerformed
        // TODO add your handling code here:
        int respuesta;
        if (!controlador.getProductos().isEmpty()) {
            respuesta = JOptionPane.showConfirmDialog(this,
                    "¿Quiere guardar los cambios en el inventario antes de salir?",
                    "Abandonar la aplicación",
                    JOptionPane.YES_NO_CANCEL_OPTION,
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            respuesta = JOptionPane.NO_OPTION;
        }
        switch (respuesta) {
            case JOptionPane.OK_OPTION:
                controlador.guardarInventarioProductos();
                cerrarAplicacion();
                break;
            case JOptionPane.NO_OPTION:
                cerrarAplicacion();
                break;
        }
    }//GEN-LAST:event_menuSalirActionPerformed

