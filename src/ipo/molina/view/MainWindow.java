    private void botonPulsado(final float cantidad) {
        if (controlador.getPedidoActual() != null) {
            botonPulsado = true;
            new Thread(new Runnable() {
                @Override
                public void run() {
                    do {
                        synchronized (MainWindow.this) {
                            try {
                                controlador.modificarPago(cantidad);
                                actualizarNumeros();
                                //300 milisegundos es una buena cantida de tiempo
                                //suficiente al menos para no confundirla con un click
                                MainWindow.this.wait(300L);
                            } catch (InterruptedException ex) {
                                Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                    } while (botonPulsado);
                }
            }).start();
        }
    }
