    private void vaciarTablaPedido() {
        DefaultTableModel model = (DefaultTableModel) tablaCompra.getModel();
        model.setRowCount(0);
    }

    public void actualizarTablaPedidos() {
        vaciarTablaPedido();
        int numProductosInsertados = controlador.getNumeroProductosEnPedido();
        DefaultTableModel model = (DefaultTableModel) tablaCompra.getModel();
        Pedido p = controlador.getPedidoActual();
        DecimalFormat df = new DecimalFormat("0.00");
        for (int i = 0; i < numProductosInsertados; i++) {
            ProductoCantidad pc = p.getProductos().get(i);
            String nombreProducto = pc.getProducto().getNombre();
            int cantidad = pc.getCantidad();
            Float precioUnidad = pc.getProducto().getPrecio();
            Object[] obj = new Object[]{nombreProducto, cantidad, df.format(precioUnidad)};
            model.insertRow(i, obj);
        }
        model.fireTableDataChanged();
    }

