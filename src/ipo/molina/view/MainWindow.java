    private void menuAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuAbrirActionPerformed
        // TODO add your handling code here:
        FileFilter f = new FileFilter() {
            @Override
            public boolean accept(File pathname) {
                if (pathname.getAbsolutePath().endsWith(".csv") || pathname.isDirectory()) {
                    return true;
                }
                return false;
            }

            @Override
            public String getDescription() {
                String msg = "Archivos CSV";
                return msg;
            }
        };
        fileChooser.setFileFilter(f);
        fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        fileChooser.showOpenDialog(this);
        File archivo = fileChooser.getSelectedFile();
        String path = archivo.getAbsolutePath();
        try {
            boolean exito = controlador.cargarDesdeCSV(path);
            if (exito) {
                mostrarMensajeExito("Se ha cargado con éxito el archivo " + path);
            } else {
                mostrarMensajeError("No se ha podido cargar el archivo " + path);
            }
        } catch (IOException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
            mostrarMensajeError("No se ha podido cargar el archivo " + path);
        }
    }//GEN-LAST:event_menuAbrirActionPerformed

